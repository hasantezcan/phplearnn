PHP Nedir?

PHP: Hypertext PreProcessor kısaltmasıdır.
Sunucu tabanlı olarak çalışır. Yani kodları derleyen, çalıştıran sunucudur.
Birçok Veritabanına uyumludur: MySQL, Oracle, ODBC vb.
Ücretsiz ve açık kaynak kodlu bir yazılımdır.

Bir PHP kodu, uzantısı *.php olan bir ASCII (düz metin) belgesinde yer alır.
Sunucu belirttiğimiz yerlerde ya da tamamında ilgili php kodlarını derler ve bu şekilde kullanıcıya iletir.

PHP en çok MySQL veritabanı ile birlikte çalıştırılır.
MySQL'de tıpkı PHP gibi açık kaynak kodludur, gelişmeye imkan tanır. Küçük ve büyük sitelerde tercih edilir.

PHP'yi seçmemizin temel nedeni, Windows, Linux gibi birçok platformda çalışması, tüm sunuculara uyumlu olması,
ücretsiz olması ve kolay öğrenilmesidir.
